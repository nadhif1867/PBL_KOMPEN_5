<script>
    $(document).ready(function() {
        $("#form-edit").validate({
            rules: {
                username: {
                    required: true,
                    minlength: 5,
                    maxlength: 30
                },
                password: {
                    required: false,
                    minlength: 5,
                    maxlength: 20
                },
                nip: {
                    required: true,
                    minlength: 3,
                    maxlength: 20
                },
                no_telepon: {
                    required: true,
                    min: 3
                },
                email: {
                    required: true,
                    minlength: 5,
                },
                nama: {
                    required: true,
                    minlength: 3,
                    maxlength: 100
                },
                avatar: {
                    required: false,
                },
            },
            submitHandler: function(form) {
                $.ajax({
                    url: form.action,
                    type: form.method,
                    data: $(form).serialize(),
                    success: function(response) {
                        if (response.status) {
                            $('#myModal').modal('hide');
                            Swal.fire({
                                icon: 'success',
                                title: 'Berhasil',
                                text: response.message
                            });
                            dataUser.ajax.reload();
                        } else {
                            $('.error-text').text('');
                            $.each(response.msgField, function(prefix, val) {
                                $('#error-' + prefix).text(val[0]);
                            });
                            Swal.fire({
                                icon: 'error',
                                title: 'Terjadi Kesalahan',
                                text: response.message
                            });
                        }
                    }
                });
                return false;
            },
            errorElement: 'span',
            errorPlacement: function(error, element) {
                error.addClass('invalid-feedback');
                element.closest('.form-group').append(error);
            },
            highlight: function(element, errorClass, validClass) {
                $(element).addClass('is-invalid');
            },
            unhighlight: function(element, errorClass, validClass) {
                $(element).removeClass('is-invalid');
            }
        });
    });
</script>
